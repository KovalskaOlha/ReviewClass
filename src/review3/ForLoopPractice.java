package review3;

public class ForLoopPractice {
    public static void main(String[] args) {
        for (int i = 10; i>5 ; i++) {
            System.out.println(i);// infinite ++ because each number will be >5
        }

        for (int i = 5; i<=1 ; i--) {
            System.out.println("happy");
        }


    }}

